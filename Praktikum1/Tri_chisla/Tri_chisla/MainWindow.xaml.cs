        private void Button_Click(object sender, RoutedEventArgs e)
        {
            if (text1.Text != "" && text2.Text != "" && text3.Text != "")
            {
                sum.Content = "Сумма = " + Math.Round((Convert.ToDouble(text1.Text) + Convert.ToDouble(text2.Text) + Convert.ToDouble(text3.Text)), 2).ToString();
                multiplier.Content = "Произведене = " + Math.Round((Convert.ToDouble(text1.Text) * Convert.ToDouble(text2.Text) * Convert.ToDouble(text3.Text)), 2).ToString();
            }
            else
            {
                MessageBox.Show("Вы не ввели все числа");
            }
        }

        public int DS_Count(string s)
        {
            string substr = System.Globalization.CultureInfo.CurrentCulture.NumberFormat.NumberDecimalSeparator[0].ToString();
            int count = (s.Length - s.Replace(substr, "").Length) / substr.Length;
            return count;
        }

